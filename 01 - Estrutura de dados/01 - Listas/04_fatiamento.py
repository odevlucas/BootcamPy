lista = ["p", "y", "t", "h", "o", "n"]
print("lista completa:",lista)
print("lista iniciando na posição dois (lista[2:]):",lista[2:])  # ["t", "h", "o", "n"]
print("lista terminando na posição dois (lista[:2]):",lista[:2])  # ["p", "y"]
print("lista começando na posição um (lembrando que o zero conta) e terminando na terceira (lista[1:3]):",lista[1:3])  # ["y", "t"]
print("lista começando na posição zero, terminando na posição três e com um pulo de 2 posições (lista[0:3:2]):",lista[0:3:2])  # ["p", "t"]
print("lista sem inicio e fim (lista[::]):",lista[::])  # ["p", "y", "t", "h", "o", "n"]
print("lista espelho, sem começo e fim e com pulo negativo de 1 (lista[::-1}):",lista[::-1])  # ["n", "o", "h", "t", "y", "p"]
